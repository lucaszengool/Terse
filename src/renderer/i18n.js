// ── Detect locale ──
function detectLocale() {
  const saved = localStorage.getItem('terse-lang');
  if (saved && SUPPORTED_LANGS.includes(saved)) return saved;

  const nav = navigator.language || navigator.languages?.[0] || 'en';
  const lower = nav.toLowerCase();

  // Chinese variants
  if (lower.startsWith('zh')) {
    if (lower.includes('hant') || lower.includes('tw') || lower.includes('hk') || lower.includes('mo')) return 'zh-Hant';
    return 'zh-Hans';
  }
  // Portuguese-BR
  if (lower.startsWith('pt')) return 'pt-BR';
  // Match first two chars
  const prefix = lower.substring(0, 2);
  const match = SUPPORTED_LANGS.find(l => l.substring(0, 2) === prefix);
  return match || 'en';
}

let _currentLang = detectLocale();

// ── Translate function ──
function t(key, params) {
  const dict = TRANSLATIONS[_currentLang] || TRANSLATIONS['en'];
  let str = dict[key] || TRANSLATIONS['en'][key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), v);
    }
  }
  return str;
}


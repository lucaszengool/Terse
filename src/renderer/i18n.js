function applyTranslations() {
  if (_currentLang === 'en') return; // No translation needed for English
  // Translate text content
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_ELEMENT);
  while (walker.nextNode()) {
    const el = walker.currentNode;
    // Only translate leaf text (no child elements with text)
    if (el.children.length === 0 || (el.childNodes.length === 1 && el.childNodes[0].nodeType === 3)) {
      const text = el.textContent.trim();
      if (EN_TEXT_MAP[text]) {
        const translated = t(EN_TEXT_MAP[text]);
        if (translated !== EN_TEXT_MAP[text]) el.textContent = translated;
      }
    }
    // Placeholders
    if (el.placeholder && EN_PLACEHOLDER_MAP[el.placeholder]) {
      el.placeholder = t(EN_PLACEHOLDER_MAP[el.placeholder]);
    }
    // Titles
    if (el.title && EN_TITLE_MAP[el.title]) {
      el.title = t(EN_TITLE_MAP[el.title]);
    }
  }
}

// ── Set language ──
function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  localStorage.setItem('terse-lang', lang);
  // Reload so translations apply from the clean English source HTML.
  // (applyTranslations() can only translate FROM English; reloading after saving
  // the preference guarantees the page re-initializes with the correct language.)
  window.location.reload();
}


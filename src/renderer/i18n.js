// ── Language picker dropdown ──
function createLangPicker(anchorEl, onPick) {
  let dd = document.getElementById('langDropdown');
  if (dd) { dd.classList.toggle('hidden'); return dd; }

  dd = document.createElement('div');
  dd.id = 'langDropdown';
  dd.className = 'lang-dropdown';
  dd.style.cssText = 'position:absolute;top:100%;right:0;z-index:1000;background:var(--sf,#fff);border:1px solid var(--bd,#e5e5e5);border-radius:8px;box-shadow:0 4px 16px rgba(0,0,0,.12);padding:4px 0;min-width:140px;max-height:300px;overflow-y:auto';

  for (const { code, label } of getLangs()) {
    const item = document.createElement('div');
    item.className = 'lang-item';
    item.dataset.lang = code;
    item.style.cssText = 'padding:6px 12px;font-size:12px;font-weight:500;cursor:pointer;display:flex;align-items:center;gap:6px;color:var(--t1,#333);transition:background .15s';
    item.innerHTML = '<span style="font-size:10px;opacity:0.4;min-width:50px">' + code + '</span><span>' + label + '</span>';
    if (code === _currentLang) {
      item.style.background = 'var(--acd,rgba(0,0,0,.05))';
      item.style.fontWeight = '700';
    }
    item.addEventListener('mouseenter', () => { item.style.background = 'var(--acd,rgba(0,0,0,.05))'; });
    item.addEventListener('mouseleave', () => {
      item.style.background = code === _currentLang ? 'var(--acd,rgba(0,0,0,.05))' : '';
    });
    item.addEventListener('click', () => {
      setLang(code);
      dd.classList.add('hidden');
      // Update active state
      dd.querySelectorAll('.lang-item').forEach(i => {
        i.style.background = '';
        i.style.fontWeight = '500';
      });
      item.style.background = 'var(--acd,rgba(0,0,0,.05))';
      item.style.fontWeight = '700';
      if (onPick) onPick(code);
    });
    dd.appendChild(item);
  }

  // Position relative to anchor
  const parent = anchorEl.parentElement;
  if (parent) {
    parent.style.position = 'relative';
    parent.appendChild(dd);
  } else {
    document.body.appendChild(dd);
  }

  // Close on outside click
  const closeHandler = (e) => {
    if (!e.target.closest('#langDropdown') && !e.target.closest('#btnLang')) {
      dd.classList.add('hidden');
    }
  };
  document.addEventListener('click', closeHandler);

  return dd;
}

